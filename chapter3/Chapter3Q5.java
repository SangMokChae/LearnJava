package chapter3;

public class Chapter3Q5 {
	public static void main(String[] args) {
		int num1 = 2;
		int num2 = 10;
		
		int num3 = 16;
		
		System.out.println(num1 & num2);
		System.out.println(num1 | num2);
		System.out.println(num1 ^ num2);
		System.out.println(~num1);
		
		System.out.println(~num3);
	}
}
